/******************************************************************************
 * Function   : LED_Init
 * Parameters : 
 * Return     : 
 * Description: 
******************************************************************************/
void LED_Init(void)
{
	DDRB |= (1<<DDB0);
	DDRB |= (1<<DDB1);
}

/******************************************************************************
 * Function   : LED_PowerOn
 * Parameters : 
 * Return     : 
 * Description: 
******************************************************************************/
void LED_PowerOn(void)
{
	PORTB |= (1<<PORTB0);
}

/******************************************************************************
 * Function   : LED_Status_Blink
 * Parameters : 
 * Return     : 
 * Description: 
******************************************************************************/
void LED_Status_Blink(void)
{
	
	PORTB ^= (1<<PORTB1);
}



